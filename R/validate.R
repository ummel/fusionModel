  # Report location of output plots, if requested
  if (!is.null(path)) cli::cli_inform("Plots saved to: {path}")

  # Assemble final result
  result <- list(plots = c(out1, out2),
                 perf = sum.perf,
                 smooth = qest,
                 data = valid)
